# input: 1 number as argument
# output: number i.e. square of the number
# Test Cases:
#   print(square(5) == 25)   # True
#   print(square(-8) == 64)  # True
# Data Structure and Algorithm:
#   a. Multiply number by itself and return it.

def square(num):
    return num * num

print(square(5) == 25)   # True
print(square(-8) == 64)  # True